    {

        $data = $request->all();
        $view = view('role-permission.form-permission')->render();

        Audit::create([
            'user_id' => auth()->user()->id,
            'event' => 'create',
            'message' => 'Created a new Permission : '.$data['name'],
        ]);

        return response()->json(['data' =>  $view, 'status'=> true]);
    }

